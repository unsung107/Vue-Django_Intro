<template>
  <div class="home">
    
  </div>
</template>

<script>
import router from '@/router'

export default {
  name: 'Home',

  methods: {

    // 사용자 로그인 유무를 확인하여 로그인 되어 있지 않을 시 로그인 페이지로 보내겠다.
    checkLoggedIn() {
      // 1. 세션을 시작해서 
      this.$session.start()

      // 2. 'jwt'가 있는지 확인
      if (!this.$session.has('jwt')) {
        // 로그인 페이지로 보내겠다.
        router.push('/login')
      }
    }
  },

  // Vue 가 화면에 그려지면 실행하는 함수
  mounted() {
    this.checkLoggedIn()
  }

}
</script>

